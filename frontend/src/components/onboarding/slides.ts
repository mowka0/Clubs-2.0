import type { HighlightTarget } from '../../hooks/useHighlight';
import type { OnboardingDoor } from '../../types/api';

/**
 * Тексты онбординга. Утверждены PO построчно (docs/modules/onboarding.md § «Слайды») —
 * менять только вместе со спекой.
 *
 * Вынесены из компонента данными, а не JSX: слайды отличаются только содержимым,
 * а разметка у всех трёх одна.
 */

export interface OnboardingPerk {
  icon: string;
  title: string;
  text: string;
}

/**
 * Главная кнопка слайда — та самая «дверь». Её тап (и только он) завершает онбординг:
 * `door` уходит на бэк метрикой намерения, `to` — куда попадает человек,
 * `highlight` — что на той странице подсветить, чтобы он запомнил, где эта кнопка живёт.
 */
export interface OnboardingDoorCta {
  label: string;
  door: OnboardingDoor;
  to: string;
  highlight: HighlightTarget;
}

/**
 * Кусок заголовка. Заголовок собран из сегментов, а не из «текст + хвост», потому что
 * название продукта стоит В СЕРЕДИНЕ слогана и набирается другим шрифтом.
 */
export interface TitleSegment {
  text: string;
  /** Акцентная (оранжевая) часть заголовка. */
  accent?: boolean;
  /** Название продукта — набирается каллиграфией Allura, чтобы читалось как имя, а не как слово. */
  brand?: boolean;
}

export interface OnboardingSlideData {
  /** Эмодзи-арт в круге-свечении. */
  art: string;
  title: TitleSegment[];
  subtitle: string;
  /** Хвост подзаголовка, набранный жирным. Есть только у слайда организатора. */
  subtitleStrong?: string;
  perks: OnboardingPerk[];
  /** Дверь. Нет на первом слайде — там только «Дальше». */
  doorCta?: OnboardingDoorCta;
  /**
   * Второстепенная кнопка: листает к СОСЕДНЕМУ слайду, онбординг не завершает.
   * Направление, а не абсолютный индекс: обе такие кнопки по смыслу и есть «соседний слайд»,
   * а зашитый номер молча уводил бы не туда, вставь кто-нибудь слайд в середину.
   */
  secondary?: { label: string; step: 1 | -1 };
}

