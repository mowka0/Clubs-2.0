-- V59: Роль «со-организатор» клуба (feature co-organizers).
-- Владелец делегирует доверенному участнику почти все полномочия по ведению клуба
-- (кроме владельческих: удаление клуба, смена ролей, СБП-реквизиты, чат-линк, биллинг).
--
-- Про ADD VALUE: изолированно, только ADD VALUE + COMMENT — БЕЗ UPDATE с новым значением
-- в этой же миграции (урок V37: PG16 разрешает ADD VALUE в транзакции Flyway, только пока
-- значение не используется в той же транзакции; прецеденты V19/V23/V43). Любой backfill,
-- проставляющий 'co_organizer' существующим строкам, обязан быть отдельной поздней миграцией.
-- IF NOT EXISTS оставляет миграцию идемпотентной, если значение уже добавлено вручную
-- для локального jOOQ codegen.
ALTER TYPE membership_role ADD VALUE IF NOT EXISTS 'co_organizer';

COMMENT ON COLUMN memberships.role IS
    'Роль в клубе: member — участник; organizer — владелец (ровно один, зеркалит clubs.owner_id, создаётся при создании клуба); co_organizer — со-организатор, назначается владельцем из активных участников, права действуют только при status = active.';
