-- V45: новый вид репутации `abandoned_slot`.
--
-- Штраф за отказ от УЖЕ ПОДТВЕРЖДЁННОГО места на Этапе 2, когда в листе ожидания нет замены —
-- участник оставил дыру (никто не занял его слот). −100 очков = половина no_show (−200):
-- предупредил заранее (отказ разрешён только ≥ порога до старта), но место так и не закрылось,
-- значит организатора всё же подставил, хоть и мягче, чем молчаливая неявка.
-- Если замена ЕСТЬ — отказ бесплатный (первый из очереди сразу поднимается), запись не создаётся.
-- Начисляется в момент отказа (Stage2Service.declineParticipation), а не на этапе явки —
-- по образцу ReputationService.penalizeExit (штраф вне attendance-потока).
--
-- IF NOT EXISTS — идемпотентность, если значение уже добавлено вручную для локального jOOQ-codegen.
-- PostgreSQL 16 разрешает ALTER TYPE ... ADD VALUE внутри Flyway-транзакции, пока новое значение
-- не используется в этой же миграции (проверено ранее на stage_2_vote/final_status, V19).

ALTER TYPE reputation_kind ADD VALUE IF NOT EXISTS 'abandoned_slot';
