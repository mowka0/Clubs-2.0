-- Онбординг: единая таксономия «категория → интерес» + признак пройденного онбординга.
--
-- Категория (club_category, 8 значений) — полка, по ней уже работают фильтры Discovery.
-- Интерес — что стоит на полке. Раньше интересы жили отдельно от клубов и сопоставлять их
-- было не с чем; теперь интерес знает свою категорию, а клуб размечается интересами.

-- ── Интерес знает свою полку ────────────────────────────────────────────────────────────
-- NULL = интерес ещё не разложен по категориям (введён в профиле вне контекста категории).
-- Такие не показываются чипами в онбординге, но живут в профиле и участвуют в подборе,
-- если совпали с интересами клуба.
ALTER TABLE interests ADD COLUMN category club_category;
COMMENT ON COLUMN interests.category IS 'Категория-полка интереса; NULL — ещё не разложен';

CREATE INDEX idx_interests_category_usage ON interests (category, usage_count DESC);
COMMENT ON INDEX idx_interests_category_usage IS 'Топ-интересы категории для чипов онбординга';

-- ── Категории пользователя (выбор в онбординге, 1–3) ────────────────────────────────────
-- Отдельно от интересов намеренно: человек может выбрать полку и не отметить ни одного чипа.
-- Выбор полки — самостоятельный факт, на нём держится подбор клубов.
CREATE TABLE user_categories (
    user_id    UUID NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    category   club_category NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (user_id, category)
);
COMMENT ON TABLE user_categories IS 'Категории, выбранные пользователем в онбординге (1–3) — основа подбора клубов';
COMMENT ON COLUMN user_categories.category IS 'Категория из club_category — совпадает с полкой клуба';

-- ── Разметка клуба интересами (0–7) ─────────────────────────────────────────────────────
-- Категория клуба остаётся одна (clubs.category) — это грубый фильтр. Интересы уточняют:
-- «Спорт» + {бег, марафон} против «Спорт» + {бокс, единоборства}.
CREATE TABLE club_interests (
    club_id     UUID NOT NULL REFERENCES clubs (id) ON DELETE CASCADE,
    interest_id UUID NOT NULL REFERENCES interests (id) ON DELETE CASCADE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (club_id, interest_id)
);
COMMENT ON TABLE club_interests IS 'Интересы клуба (0–7) — точность подбора поверх категории';

CREATE INDEX idx_club_interests_interest ON club_interests (interest_id);
COMMENT ON INDEX idx_club_interests_interest IS 'Обратный проход: по интересу пользователя — какие клубы совпали';

-- ── Признак пройденного онбординга ──────────────────────────────────────────────────────
-- Явная колонка, а не производное «нет интересов → показать онбординг»: производный признак
-- ломается при ошибке запроса (данные не пришли ≠ данных нет) и показывает онбординг поверх
-- заполненного аккаунта. Заодно честная метрика воронки.
-- Существующим пользователям остаётся NULL — они пройдут онбординг при следующем входе
-- (города и категорий у них нет, без них подбор не работает; уже заполненное предзаполнится).
ALTER TABLE users ADD COLUMN onboarded_at TIMESTAMPTZ;
COMMENT ON COLUMN users.onboarded_at IS 'Когда пользователь завершил онбординг; NULL — ещё не проходил';

