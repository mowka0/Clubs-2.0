-- Оживление статуса expired для просроченного продления (решение PO 2026-07-06,
-- docs/modules/membership-lifecycle.md). Целевая модель:
--   frozen  = новый платный участник ждёт ПЕРВОГО взноса (единственный смысл);
--   expired = продление просрочено — доступ был, оплаченное окно истекло (шедулер active → expired).
--
-- Разовый флип легаси-должников: строки, которые старый шедулер (или организатор кнопкой
-- «Закрыть доступ» эпохи до 2026-07-06) заморозил ПОСЛЕ уже выданного оплаченного окна.
-- Отличительный признак должника: окно доступа существует и уже в прошлом.
-- НЕ трогаем: frozen с NULL-окном (настоящее первое вступление, окно ещё не выдавалось)
-- и frozen с будущим окном (редкая ручная пауза при живом окне — оставляем как есть).
UPDATE memberships
SET status = 'expired', updated_at = now()
WHERE status = 'frozen'
  AND subscription_expires_at IS NOT NULL
  AND subscription_expires_at <= now();

-- Уборка легаси grace_period (решение PO 2026-07-06): статус Stars-эпохи «оплата просрочена,
-- идут ретраи» никем не пишется с de-Stars, но застрявшие строки существуют (ручной тест PO
-- 2026-07-06 нашёл такую) и были тупиком: ни шедулер, ни UI их не трогали. Семантика совпадает
-- с новым expired (доступ был, оплата просрочена) — флипаем безусловно. После этого grace_period
-- вычищен из всех read-фильтров кода; значение остаётся в типе только потому, что Postgres не
-- умеет DROP VALUE у енама (пересоздание типа ради косметики не оправдано).
UPDATE memberships
SET status = 'expired', updated_at = now()
WHERE status = 'grace_period';

-- Актуализация описания жизненного цикла (V37 описывал expired как «легаси, не используется»).
COMMENT ON COLUMN memberships.status IS
    'Жизненный цикл членства: active = состоит и есть доступ к контенту | frozen = новый платный участник ждёт первого взноса (доступ закрыт) | expired = продление просрочено — доступ был, окно истекло, участник-должник остаётся в клубе (шедулер переводит active → expired ежедневно) | cancelled = вышел из клуба / удалён | grace_period = МЁРТВОЕ легаси Stars-эпохи: не пишется и не читается кодом, строки флипнуты в expired (V46), значение не удалено из типа только потому, что Postgres не умеет DROP VALUE.';
