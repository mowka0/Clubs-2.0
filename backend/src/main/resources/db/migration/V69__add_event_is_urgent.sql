-- Бейдж формата на карточках встреч (решение PO 2026-07-23): срочной встрече нужен явный
-- признак — она рождается сразу в stage_2 и без флага неотличима от обычного события,
-- чей Этап 2 уже стартовал. Флаг влияет только на подачу (бейдж «⚡ срочная»), механика
-- Этапа 2 и репутация — как у обычного события с местами.
ALTER TABLE events ADD COLUMN IF NOT EXISTS is_urgent BOOLEAN NOT NULL DEFAULT FALSE;

COMMENT ON COLUMN events.is_urgent IS
    'Срочная встреча (формат PO 2026-07-23): создана без Этапа 1, сразу в stage_2. Только подача (бейдж «⚡ срочная» на карточках); механика и репутация как у обычного события с местами. У открытых встреч всегда FALSE.';
