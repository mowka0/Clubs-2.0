-- Поднимаем дефолт voting_opens_days_before с 5 до 14 дней.
-- Кейсы организации мероприятий показали что 5 дней слишком короткое окно
-- для голосования (участники не успевают увидеть событие и проголосовать).
-- 14 дней (2 недели) — более sensible default. CHECK-границы (1..14)
-- сохраняем — 14 = текущий максимум.
--
-- Затрагивает только новые INSERT'ы без явного значения (через DB default).
-- Все существующие events сохраняют свои значения. Kotlin-уровень defaults
-- (EventDto, EventService, VoteService) обновлён в том же PR — теперь они
-- тоже отдают 14 если значение не указано.

ALTER TABLE events
    ALTER COLUMN voting_opens_days_before SET DEFAULT 14;
