package com.clubs.common.auth

/**
 * Единица авторизации в клубе — capability (право на класс операций), а не отдельный эндпоинт.
 * Роль клуба = именованный набор capabilities (карта в [RoleCapabilities]); гейты проверяют
 * capability, а не имя роли, поэтому новая роль (модератор и т.п.) встаёт одной записью в карте,
 * не трогая 45 точек авторизации (docs/modules/club-roles.md § 1).
 *
 * 14 значений: 9 делегируемых (входят в набор co_organizer) + 5 владельческих (только у владельца).
 */
enum class ClubCapability {

    // --- Делегируемые (входят в набор co_organizer) ---

    /** Заявки клуба: инбокс и счётчики, одобрить, отклонить (с причиной), «Расширить клуб и принять всех». */
    APPROVE_APPLICATIONS,

    /** События: создать, отменить, отметить посещаемость, разрешать споры, видеть dispute-note откликнувшихся. */
    MANAGE_EVENTS,

    /** Складчины клуба: создать, вести, отметить оплату, resolve-decline, закрыть — по ЛЮБОМУ сбору клуба (У-1). */
    MANAGE_SKLADCHINA,

    /**
     * Участники (role = member): заморозка/разморозка, «взнос получен»/«не получен», reject-dues,
     * своя дата доступа (access-until), заметка, кик; менеджерский вид ростера и карточки участника.
     */
    MANAGE_MEMBERS,

    /** Награды участнику (role = member): выдать, снять, видеть подсказки-награды. */
    GRANT_AWARDS,

    /**
     * Настройки клуба: название, описание, город, правила, лимит участников, аватар.
     * БЕЗ СБП-реквизитов и БЕЗ перехода в платный (это [EDIT_PAYMENT_REQUISITES]).
     */
    EDIT_CLUB_SETTINGS,

    /** Просмотр финансов клуба (GET /finances). */
    VIEW_FINANCES,

    /** Просмотр статистики/аналитики клуба и списка ушедших (stats, churned-members). */
    VIEW_STATS,

    /** Личные приглашения (invite-share, prepared message) и перегенерация инвайт-ссылки. */
    SEND_INVITES,

    // --- Владельческие (только у organizer, НЕ делегируются) ---

    /** Назначать/снимать роли участникам (PUT .../role). Единственный владелец права — владелец клуба. */
    MANAGE_ROLES,

    /**
     * СБП-реквизиты клуба (paymentLink, paymentMethodNote) и перевод бесплатного клуба в платный.
     * Деньги взносов идут владельцу — подмена реквизитов = увод взносов.
     */
    EDIT_PAYMENT_REQUISITES,

    /** Привязка/отвязка/настройка Telegram-чата клуба (chat-link), включая бот-сторону (/link). */
    MANAGE_CHAT,

    /**
     * Организаторская подписка/биллинг (планы-ёмкости). Payer = владелец. Сейчас гейтится user-scope'ом
     * (SubscriptionController), club-scoped точек нет — capability для полноты владельческого набора и
     * как дом для будущих club-scoped биллинг-действий.
     */
    MANAGE_BILLING,

    /** Мягкое удаление клуба (каскад по событиям/складчинам/заявкам). */
    DELETE_CLUB
}
